class Employee {
  final String name;
  final String team;
  final int tipsAmount;
  final int advance;
  final int hours;
  final String image;
  final double percent;

  Employee({
    required this.name,
    required this.team,
    required this.tipsAmount,
    required this.advance,
    required this.hours,
    required this.image,
    required this.percent,
  });
}
